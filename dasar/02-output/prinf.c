// %s	untuk menampilkan teks (string)
// %d, %i	untuk menampilkan bilangan desimal
// %f	untuk menampilkan bilangan pecahan
// %o	untuk menampilkan bilangan oktal
// %x	untuk menampilkan bilangan heksadesimal
